    public boolean isEmptyStudent(){
        if(jTextField2.getText().isEmpty()|| jTextField3.getText().isEmpty()|| jTextField4.getText().isEmpty()|| jTextField5.getText().isEmpty()
                || jTextField6.getText().isEmpty()|| jTextField7.getText().isEmpty()|| jTextField8.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"All Fields are required");
            return false;
        }
        if(!jTextField3.getText().matches("^.+@.+\\...+$")){
            JOptionPane.showMessageDialog(this,"Invalid Email Address");
            return false;
        }
        if(jTextField4.getText().length()>10){
            JOptionPane.showMessageDialog(this,"Phone Number is too long");
            return false;
        }
        return true;
    }
    
